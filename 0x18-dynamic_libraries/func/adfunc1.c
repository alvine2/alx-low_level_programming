#include <stdio.h>

/**
 * add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
*/

int add(int a, int b)
{
	return (a + b);
}

/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: product of a and b
*/

int mul(int a, int b)
{
	return (a * b);
}
